    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonAceptarActionPerformed
    {//GEN-HEADEREND:event_jButtonAceptarActionPerformed
        String nombreCuenta = jTextFieldNombreCuenta.getText();
        String correo = jTextFieldCorreo.getText();
        String contrasenia = jTextFieldContrasenia.getText();

        if ((!nombreCuenta.equals("")) && (!correo.equals("") && (!contrasenia.equals(""))))
        {
            Cuenta obj = new Cuenta(nombreCuenta, correo, contrasenia);

            Nodo aux = new Nodo(obj, nombreCuenta);
            String[] etiqueta = new String[1];
            etiqueta[0] = nombreCuenta;

            i.insertaMultilista(aux, etiqueta);

            NodoArbol nuevo = new NodoArbol(nombreCuenta, aux);
            i.insertaArbol(nuevo);
            padre.crearCuenta();
        }
        
        jTextFieldNombreCuenta.setText("");
        jTextFieldCorreo.setText("");
        jTextFieldContrasenia.setText("");
        this.dispose();
        

//        aceptarRegresarInterfaz(evt);
    }//GEN-LAST:event_jButtonAceptarActionPerformed

